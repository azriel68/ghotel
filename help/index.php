<h3><a name="5" id="5"></a>R&eacute;servation</h3>
<p><img src="../images/help/8-resa.jpg" width="1026" height="633" /> <br />
  La r&eacute;servation est relative &agrave; un client. Une r&eacute;servation concerne un et un seul client ainsi qu'une seule chambre (ainsi, une m&ecirc;me famille louant 2 chambres se verra attitr&eacute;e 2 r&eacute;servations). C'est &eacute;galement &agrave; ce niveau que vous pourrez ajouter des prestations suppl&eacute;mentaire non comprises dans le prix de la chambre (tel que le petit d&eacute;jeuner ou encore une place de parking). <br />
  <br />
  <img src="../images/help/9-resa.jpg" width="806" height="331" /><br />
Vous pourrez faire tout vos choix grace &agrave; des popups.</p>
<p><img src="../images/help/9-resa-pop-chambre.jpg" width="604" height="318" /><br />
    <br />
  <img src="../images/help/9-resa-pop-cli.jpg" width="408" height="297" /> <img src="../images/help/9-resa-prod.jpg" width="408" height="297" /></p>
<h3><a name="6" id="6"></a>Planning</h3>
<p>Le planning est le coeur de l'application. C'est lui qui vous permmettra d'un coup d'oeil de voir les r&eacute;servations en cours et &agrave; venir et d'en saisir rapidement de nouvelles.</p>
<p><img src="../images/help/4-planning.jpg" width="942" height="538" /> Un code couleur vous indique si une r&eacute;servation est prise, confirm&eacute;e ou pay&eacute;e. Vous pouvez directement &agrave; partir de cette &eacute;cran d&eacute;placer une r&eacute;servation, la rallonger ou la raccourcir. Vous pouvez &eacute;galement l'&eacute;diter ou la supprimer. </p>
<h3><a name="7" id="7"></a>Les factures</h3>
<p>Le module de facturation est un &eacute;l&eacute;ment pr&eacute;pond&eacute;rant &agrave; la vie de votre hotel. C'est pourquoi il se veut le plus souple possible pour vous laisser toutes lattitude au moment de l'encaissement du client.</p>
<p><img src="../images/help/11-facture.jpg" width="810" height="417" /> <br />
Ainsi chaque ligne pourra &ecirc;tre personnalis&eacute;e (libell&eacute;, quantit&eacute;, prix). Des lignes libres pourront m&ecirc;me &ecirc;tre rajout&eacute;es. Une remise ainsi qu'un &eacute;ventuel montant n&eacute;goci&eacute; peuvent &eacute;galement &ecirc;tre saisis.<br />
  <br />
<img src="../images/help/12-facture.jpg" width="849" height="617" /><br />
En finalit&eacute; vous obtenez une &eacute;dition au format PDF que vous pouvez imprimer et/ou envoyer au client par e-mail (ou encore la stocker sur votre ordinateur pour vos archives). </p>
