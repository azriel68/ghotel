<p><img src="../images/help/15-produit.jpg" width="819" height="217" /> </p>
<h4><a name="8_2" id="8_2"></a>Les chambres</h4>
<p><img src="../images/help/12-chambre.jpg" width="1047" height="216" /></p>
<p>La chambre poss&eacute;de diff&eacute;rente information qui vous permette de choisir convenablement pour chaque client la chambre qui lui conviendra. Notez que le prix de la chambre est personnalisable au cas par cas. </p>
<p><img src="../images/help/13-chambre.jpg" width="818" height="264" /></p>
<h4><a name="8_3" id="8_3"></a>Les cat&eacute;gories </h4>
<p>Chaque cat&eacute;gorie porte un nom et dispose d'un tarif par d&eacute;faut qui sera assign&eacute; automatique &agrave; une chambre de cette cat&eacute;gorie (&agrave; noter que le tarif pour chaque chambre est personnalisable, celui-ci prime sur celui de la cat&eacute;gorie). </p>
<p><img src="../images/help/14-categ.jpg" width="813" height="227" /></p>
