<h3><a name="1" id="1"></a>Fonctionnalit&eacute;s de bases</h3>
<h4><a name="1_1" id="1_1"></a>L'identification</h4>
<p><img src="../images/help/1-accueil.jpg" width="787" height="226" /></p>
<p>Cette page vous permet de saisir l'identifiant et le mot de passe qui vous aura &eacute;t&eacute; communiqu&eacute; lors de votre inscription. C'est &eacute;galement sur cet &eacute;cran que vous reviendrez lorsque vous vous d&eacute;connecterez. </p>
<h4><a name="1_2" id="1_2"></a>Le menu</h4>
<p><img src="../images/help/menu.jpg" width="820" height="54" /></p>
<p>Ce menu est en permanence pr&eacute;sent en haut des &eacute;crans de consultation et de saisie (&agrave; l'exception de l'&eacute;cran d'identification, de l'&eacute;cran de choix de l'hotel &agrave; g&eacute;rer et du menu administrateur). Il vous permet de naviguer tr&egrave;s simplement entre les diverses partie de l'application sans repasser par une page d'accueil. </p>
<h4><a name="1_3" id="1_3"></a>Les boutons et &eacute;crans  standards</h4>
<p>Chaque module se pr&eacute;sente de la m&ecirc;me mani&egrave;re (&agrave; l'exception du planning).<br />
  L'&eacute;cran pr&eacute;sente en premier lieu la liste des &eacute;l&eacute;m&eacute;nts modifiable (par exemple pour les clients il pr&eacute;sente la liste des clients)
</p>
<p><img src="../images/help/6-client.jpg" width="944" height="319" /></p>
<p>Cette liste est triable sur tous les champs visible. Par ailleur certain &eacute;crans s'accompagne d'une recherche intuitive (sur les champs courants de chaque objet) et/ou d'un index alphabetique. Un simple clique sur une ligne vous am&egrave;ne &agrave; l'&eacute;cran de saisie associ&eacute;.</p>
<p><img src="../images/help/7-client.jpg" width="813" height="528" /> <br />
  Notez que chaque fiche de saisie se comporte de la m&ecirc;me mani&egrave;re. Un libell&eacute; indique devant chaque champs ce qu'il doit contenir. Une date de modification et de cr&eacute;ation est pr&eacute;sente sur chaque fiche. Enfin vous retrouverez toujours les trois boutons du bas permettant de retrouner &agrave; la liste d'objet, de supprimer l'objet courant ou de valider les modifications courante. <br />
</p>
<h3><a name="2" id="2"></a>Utilisateurs et groupes </h3>
<p>L'application est multi-utilisateurs et multi-hotel. C'est pourquoi la gestion des utilisateurs a &eacute;t&eacute; pens&eacute;e de telle mani&egrave;re que vous puissiez g&eacute;rer avec aissante qui de vos collaborateur peut acc&eacute;der &agrave; tel ou tel hotel ou bien encore l'&eacute;tendue de ses pouvoirs.</p>
<p><img src="../images/help/16-user.jpg" width="809" height="279" /> </p>
<p>Vous pourrez d&eacute;finir pour chaque utilisateur un identidiant/login et un mot de passe lui permettant de rentrer dans l'application. Pour chacun vous pourrez d&eacute;finir s'il s'ag&icirc;t d'un utilsateur avec pouvoir ou simplement un intervenant de saisie (et pour chacun d&eacute;finir les hotels affili&eacute;s). A noter que tout utilisateurs de type &quot;Principal&quot; &agrave; ce pouvoir. </p>
<h3><a name="3" id="3"></a>Hotel</h3>
<p>L'application &eacute;tant multi-hotel, il vous faudra pr&eacute;alablement &agrave; la gestion de l'un, le s&eacute;lectionner. Cela se fait tr&egrave;s simplement sur la premi&egrave;re page de l'application, une fois identifi&eacute;.</p>
<p><img src="../images/help/2-choix-hotel.jpg" width="788" height="250" /> </p>
<p>Notez que c'est &eacute;galement via cet &eacute;cran que vous pourrez cr&eacute;er vos hotels. Par une simple clique vous acc&egrave;dez aux informations de l'hotel (n&eacute;ccessaire &agrave; la facturation) et &agrave; toutes les fonctionnalit&eacute;s attenantes.</p>
<p><img src="../images/help/3-hotel.jpg" width="940" height="780" /></p>
<h3><a name="4" id="4"></a>Client</h3>
<p>La base de la vie &eacute;conomique d'un hotel repose sur le client. C'est pourquoi il vous faut pouvoir g&eacute;rer vos clients ainsi que tout ce qui en d&eacute;coulera.</p>
<p><img src="../images/help/6-client.jpg" width="944" height="319" /> </p>
<p>La fiche client elle vous apporte, en plus des informations relatives au client, la liste de ses r&eacute;servations d&eacute;j&agrave; pass&eacute;s ainsi que la liste des factures le concernant.</p>
<p><img src="../images/help/7-client.jpg" width="813" height="528" /> <br />
    <br />
  <br />
  <img src="../images/help/7-clientb.jpg" width="984" height="536" /></p>
